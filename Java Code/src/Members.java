
public class Members {
	String fName;
	String lName;
	String address;
	int userId;
	String phone;
	String email;
	String startDate;
	int warehouseDistance;

	public Members(String fName, String lName, String address, int userId, String phone, String email, 
			String startDate, int warehouseDistance) {
		this.fName = fName;
		this.lName = lName;
		this.address = address;
		this.userId = userId;
		this.phone = phone;
		this.email = email;
		this.startDate = startDate;
		this.warehouseDistance = warehouseDistance;
	}
}
